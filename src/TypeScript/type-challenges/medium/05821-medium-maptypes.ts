type MapTypes1<T, R extends { mapFrom: any; mapTo: any }> = {
  [K in keyof T]: T[K] extends R['mapFrom'] ? R['mapTo'] : T[K];
};

type MapTypes<T, R extends { mapFrom: any; mapTo: any }> = {
  [K in keyof T]: T[K] extends R['mapFrom']
    ? R extends { mapFrom: T[K] }
      ? R['mapTo']
      : never
    : T[K];
};

type a = MapTypes<{ stringToArray: string }, { mapFrom: string; mapTo: [] }>;

type b = MapTypes<
  { date: string },
  { mapFrom: string; mapTo: Date } | { mapFrom: string; mapTo: null }
>;

type c = MapTypes1<
  { name: string; date: Date },
  { mapFrom: string; mapTo: boolean } | { mapFrom: Date; mapTo: string }
>;

